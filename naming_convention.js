/**
 *Naming Conventions

 1) 영어를 사용하면 문자와 숫자를 모두 사용 가능
 2) 특수기호는 _ 와 $만 사용 가능
 3) 숫자로 이름을 시작할 수 없다
 4) 키워드는 변수명으로 사용 할 수 없다
 */

let name = "방재운";
var $sport = "축구";
const _cook = "요리";

console.log(name);
console.log($sport);
console.log(_cook);

// 가능

// let 1cook = "요리" 불가능 . 3
// var const = "아무거나" 불가능 . 4

